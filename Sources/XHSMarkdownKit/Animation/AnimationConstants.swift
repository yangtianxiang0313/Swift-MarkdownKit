import UIKit

/// 动画相关常量
public enum AnimationConstants {
    /// 初始隐藏时的 alpha
    public static let initialAlpha: CGFloat = 0
    /// 显示完成时的 alpha
    public static let visibleAlpha: CGFloat = 1
    /// 零延迟
    public static let zeroDelay: TimeInterval = 0
    /// 无水平位移
    public static let noTranslation: CGFloat = 0
}
